/* Printing numbers using loop */

#include <stdio.h>

/* main which program begins execution */
int main(void)
{
   /* Variable declaration and initialization */
   int num = 1;

   /* Program Process */
   // while number less or equal to 10
   while (num <= 10)
   {
      printf("%d   ", num);
      num++;
   }

   return 0;
}
